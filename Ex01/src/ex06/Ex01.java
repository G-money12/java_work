package ex06;

public class Ex01 {
	private String name;
	String adress;
	private int phonNum;
	
	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
	public void setAdress(String adress) {
		this.adress=adress;
	}
	public String getAdress() {
		return adress;
	}
	public void setPhonNum(int phonNum) {
		this.phonNum=phonNum;
	}
	public int getPhonNum() {
		return phonNum;
	}

}
